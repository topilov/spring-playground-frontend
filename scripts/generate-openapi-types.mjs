import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

import openapiTS, { astToString } from 'openapi-typescript';
import { loadEnv } from 'vite';

const DEFAULT_SCHEMA_URL =
  'https://topilov.github.io/spring-playground-backend/openapi/openapi.json';
const DEFAULT_FALLBACK_SCHEMA_URL =
  'https://raw.githubusercontent.com/topilov/spring-playground-backend/main/openapi/openapi.yaml';
const OUTPUT_FILE = path.resolve(process.cwd(), 'src/shared/api/generated/schema.d.ts');

function readSchemaUrls() {
  const env = loadEnv(process.env.MODE ?? 'development', process.cwd(), '');

  return {
    primary:
      process.env.VITE_API_SCHEMA_URL ||
      env.VITE_API_SCHEMA_URL ||
      DEFAULT_SCHEMA_URL,
    fallback:
      process.env.OPENAPI_FALLBACK_SCHEMA_URL ||
      env.OPENAPI_FALLBACK_SCHEMA_URL ||
      DEFAULT_FALLBACK_SCHEMA_URL,
  };
}

async function generateTypes(schemaUrl) {
  const ast = await openapiTS(new URL(schemaUrl));
  return astToString(ast);
}

async function main() {
  const { primary, fallback } = readSchemaUrls();
  let schemaSource = primary;
  let output;

  try {
    output = await generateTypes(primary);
  } catch (primaryError) {
    if (primary === fallback) {
      throw primaryError;
    }

    console.warn(
      `[openapi:generate] Unable to read primary schema at ${primary}. Falling back to ${fallback}.`
    );
    schemaSource = fallback;
    output = await generateTypes(fallback);
  }

  await mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await writeFile(
    OUTPUT_FILE,
    `/**\n * Generated by openapi-typescript from ${schemaSource}\n * Do not edit this file manually.\n */\n\n${output}`,
    'utf8'
  );

  console.log(`[openapi:generate] Wrote ${OUTPUT_FILE} from ${schemaSource}`);
}

main().catch((error) => {
  console.error('[openapi:generate] Failed to generate types.');
  console.error(error);
  process.exitCode = 1;
});
